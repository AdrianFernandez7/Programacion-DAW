public class Prueba {
    public static void main(String[] args) {
        int vector[]={1,2,3};
        int contador=0;
        for(int i=0;i<vector.lenght;i++){
            if(vector[i]==2) contador++;
        }
        int vector2[]=new int[vector.lenght-contador];
        for(int i=0;i<vector2.lenght;i++){
            if(vector[i]!=2){
                vector2[i]=vector[i];
            }
        }  
    }
}
